#include "libft.h"

char	*ft_strsub_free(char const *s, unsigned int start, size_t len)
{
	char			*sub;
	unsigned int	i;

	if (!(sub = ft_strnew(len)))
		return (NULL);
	i = 0;
	while (i < len)
	{
		sub[i] = s[start + i];
		i++;
	}
	sub[i] = 0;
	ft_strdel((char**)(&s));
	return (sub);
}
